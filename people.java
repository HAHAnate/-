class people {

    private int ID;
    private String name;
    private String age;
    

    public people (int ID, String name, String age) {
        this.ID = ID;
        this.name = name;
        this.age = age;
    }

    public int getID() {
        return ID;
    } 


    public String getname(){
        return name;
    }

    public String age(){
        return age;
    }
    
    
}
